void solve()
{
    int x; vector<int> a;
    ifstream inp("./input.txt");
    while(inp >> x) a.pb(x);
    inp.close();

    int ans = INT_MIN, comp = 0;
    for (int i = 0; i < a.size(); i++) {
        comp = comp + a[i];
        if (ans < comp)
            ans = comp;
 
        if (comp < 0)
            comp = 0;
    }

    ofstream outputFile("output.txt");
	outputFile << ans;
    outputFile.close();
}

